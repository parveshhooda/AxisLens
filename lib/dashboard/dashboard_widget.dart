              (_model.apiResultj1o?.jsonBody ?? ''),
            ) ==
            0) {
          setState(() {
            FFAppState().LP = 0;
          });
          setState(() {
            FFAppState().isLoading = false;
          });
        } else {
          setState(() {
            FFAppState().RewardsValue =
                _model.simpleSearchResults.first.rewardValue;
            FFAppState().AcceptedAPIPriorities =
                (GetAcceptedIssuesCall.priorityList(
              (_model.apiResultj1o?.jsonBody ?? ''),
            ) as List)
                    .map<String>((s) => s.toString())
                    .toList()!
                    .toList()
                    .cast<String>();
          });
          _model.lp = await actions.findTotalLP();
          setState(() {
            FFAppState().LP = valueOrDefault<int>(
              _model.lp,
              0,
            );
          });
          setState(() {
            FFAppState().isLoading = false;
          });
        }

        return;
      } else {
        setState(() {
          FFAppState().APIFialureFlag = false;
        });
        setState(() {
          FFAppState().isLoading = false;
        });

        context.goNamed('Failure');
      }
    });

